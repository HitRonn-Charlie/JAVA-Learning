class Strings {

    public static void main(String[] args) {
        
    
    //Create String

     String first = "Java ";
     String second = "Programming";


   
   
   // String length

   int Length = first.length();

   System.out.println("Length of String: " + Length);





   //Join string two method

   System.out.println(first + second);

   String joinString = first.concat(second);
   
   System.out.println("Joined string: " + joinString);


   

   //Compare String two method

   boolean Compare = first.equals(second);

   System.out.println("String first and second are equals: " + Compare);

  
   
   
   
    }




}
